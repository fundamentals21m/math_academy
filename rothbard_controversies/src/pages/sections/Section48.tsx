      <p>
        Friedman led the "Chicago School" of economics, which differs significantly from the
        Austrian School. The Chicago School accepts the mathematical formalism of mainstream
        economics; the Austrians reject it. The Chicago School accepts empirical testing as
        the criterion of truth; the Austrians emphasize logical deduction. The Chicago School
        often supports government intervention to correct "market failures"; the Austrians
        are more consistently anti-interventionist.
      </p>

